-- 7. Tabela de Perfis de Embeddings Faciais
CREATE TABLE IF NOT EXISTS face_embeddings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES "Users"(id) ON DELETE CASCADE,
    nome_guerra VARCHAR(100) NOT NULL,
    telegram_id VARCHAR(50) UNIQUE,
    embedding JSONB NOT NULL, -- Vetor 512D do InsightFace
    drive_folder_id VARCHAR(100) NULL,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 8. Tabela de Registro de Fotos Processadas (Google Drive)
CREATE TABLE IF NOT EXISTS processed_photos (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    event_name VARCHAR(150) NOT NULL,
    filename VARCHAR(250) NOT NULL,
    drive_file_id VARCHAR(100) NULL,
    drive_link TEXT NULL,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 9. Tabela de Correspondências Faciais e Moderação
CREATE TABLE IF NOT EXISTS photo_matches (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    photo_id UUID REFERENCES processed_photos(id) ON DELETE CASCADE,
    militar_id UUID REFERENCES "Users"(id) ON DELETE CASCADE,
    similarity REAL NOT NULL,
    status VARCHAR(30) DEFAULT 'aprovado', -- 'aprovado', 'pendente', 'rejeitado'
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);
